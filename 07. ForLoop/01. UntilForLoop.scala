// scala program for until for loop

// used when prior knowlwdge of iteration's number
// until use to when we want to print the values from 0 to n-1

// creating object
object MainObject {
	// main method
	def main(args: Array[String]) {
		// initialization 
        var a = 0;
        // until for loop
        for (a <- 1 until 10) {
            println(a)
        }
   }  
}  